def network():  # function named network calling network will start this function to provide the extended version of an acronym founded in the dictionary if their is not one found
                # the function will prompt the user to input the acronym into the dictionary 
    while True:
        print('Please Enter A Networking or Security Related Acronym To Get Started:\n (blank to quit) ') # prompts the user to input a networking acronym then checks to see
        acronym = input().upper() # .upper makes it so if the user inputs lower case the script will input it into uppercase 
        if acronym == '': # if the user enters nothing the loop will break
            break

        if acronym in acronym_dict:
            print(acronym_dict[acronym] + ' is the acronym of ' + acronym) # if the acronym that the user inputs matches one inside the dictionary the script will output that
            break
        try:
            print('I do not have an acronym stored for that ') 
            print('Re-Enter the acronym ?')# if their is not a acronym found in the dictionary this is the part of the script that starts to prompt the user to enter new key and key pair
            key = input().upper() 
        except KeyboardInterrupt: # Exception handling if the user decided to ctrl+c to cancel out of the script
            pass # this will pass the exception and continue on with the loop
        try:
            print("What is the acronym's definition") # asking the user what the definition for the acronym is
            value = input()
        except KeyboardInterrupt: # Exception handling like above to prevent from the user ctr+c out of the script and to continue on with the script
            pass
            acronym_dict.update({key: value}) # updates the dictionary with the key and key pair in the dictionary
            print('Acronym database updated.')
            continue # This will loop back to the beginning prompting the user to input an acronym if the user chooses to thats where they can leave the prompt blank to quit


network() # Calling the function

